for (i64 l = 1, r = 0; l <= n; l = r + 1) {
    i64 res = n / l;
    r = n / res;
    // 对于区间 [l, r] 内的所有整数 i，n / i 的结果（向下取整）都相同。
    // in [l, r] :
}

